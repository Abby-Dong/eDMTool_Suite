// Set language and re-render everything
function setLanguage(lang) {
  currentLang = lang;
  // Update switcher UI
  const labels = { 'en': 'English', 'zh-TW': '繁體中文', 'ko': '한국어' };
  document.getElementById('currentLangLabel').textContent = labels[lang] || 'English';
  // Update active state in dropdown
  document.querySelectorAll('.lang-option').forEach(opt => {
    opt.classList.toggle('active', opt.dataset.lang === lang);
  });
  // Close dropdown
  document.getElementById('langSwitcher').classList.remove('open');
  // Apply static translations
  applyStaticTranslations();
  // Re-render dynamic views
  const bv = document.getElementById('builderView');
  const clv = document.getElementById('compLibraryView');
  if (bv && bv.classList.contains('visible')) {
    document.getElementById('breadcrumbName').textContent = t('breadcrumb.customizeEdm');
    renderBuilderLeft();
    renderBuilderPreview();
    renderBuilderRight();
  } else if (clv && clv.classList.contains('visible')) {
    document.getElementById('breadcrumbName').textContent = t('compLibrary.title');
    if (currentCompId) {
      renderCompLibraryTabs(currentCompId);
      renderCustomizer(currentCompId);
    }
  } else {
    renderGallery();
  }
  // Set html lang attribute
  document.documentElement.lang = lang === 'zh-TW' ? 'zh-TW' : lang === 'ko' ? 'ko' : 'en';
  // Add Korean font if needed
  if (lang === 'ko') {
    document.body.style.fontFamily = "'Noto Sans KR', 'Inter', -apple-system, BlinkMacSystemFont, 'Segoe UI', Roboto, sans-serif";
  } else {
    document.body.style.fontFamily = "";
  }
}
